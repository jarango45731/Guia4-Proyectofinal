    private void buttonDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDepositarActionPerformed
        double cantidad;
        try {
            cantidad = Double.parseDouble(this.textFieldCantidadDepositar.getText());
        } catch (NumberFormatException | NullPointerException nfe) {
            JOptionPane.showMessageDialog(null, "Cantidad invÃ¡lida");
            return;
        }
        if (cantidad < 0) {
            JOptionPane.showMessageDialog(null, "Cantidad invÃ¡lida");
            return;
        }
        this.db.depositar(this.cuenta.getNumero(), cantidad);
        this.cuenta.depositar(cantidad);
        JOptionPane.showMessageDialog(null, "Cantidad depositada correctamente");
        this.textFieldSaldo.setText(Double.toString(this.cuenta.getSaldo()));
        this.textFieldCantidadDepositar.setText("");
    }//GEN-LAST:event_buttonDepositarActionPerformed

    private void buttonRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRetirarActionPerformed
        // TODO add your handling code here:
        double cantidad;
        try {
            cantidad = Double.parseDouble(this.textFieldRetiro.getText());
        } catch (NumberFormatException | NullPointerException nfe) {
            JOptionPane.showMessageDialog(null, "Cantidad invÃ¡lida");
            return;
        }
        if (cantidad < 0) {
            JOptionPane.showMessageDialog(null, "Cantidad invÃ¡lida");
            return;
        }
        if (cantidad > this.cuenta.getSaldo()) {
            JOptionPane.showMessageDialog(null, "La cantidad a retirar excede el saldo");
            return;
        }
        this.db.retirar(this.cuenta.getNumero(), cantidad);
        this.cuenta.retirar(cantidad);
        JOptionPane.showMessageDialog(null, "Retiro efectuado correctamente");
        this.textFieldSaldo.setText(Double.toString(this.cuenta.getSaldo()));
        this.textFieldRetiro.setText("");
    }//GEN-LAST:event_buttonRetirarActionPerformed

    private void buttonTransferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTransferirActionPerformed
        // TODO add your handling code here:
        String cuentaDestino = this.textFieldCuentaTransferir.getText();
        if (this.cuenta.getNumero().equals(cuentaDestino) || !db.existe(cuentaDestino)) {
            JOptionPane.showMessageDialog(null, "Cuenta destino invÃ¡lida");
            return;
        }
        double cantidad;
        try {
            cantidad = Double.parseDouble(this.textFieldCantidadTransferir.getText());
        } catch (NumberFormatException | NullPointerException nfe) {
            JOptionPane.showMessageDialog(null, "Cantidad invÃ¡lida");
            return;
        }
        if (cantidad < 0) {
            JOptionPane.showMessageDialog(null, "Cantidad invÃ¡lida");
            return;
        }
        if (cantidad > this.cuenta.getSaldo()) {
            JOptionPane.showMessageDialog(null, "La cantidad a transferir excede el saldo");
            return;
        }
        this.db.transferir(this.cuenta.getNumero(), cuentaDestino, cantidad);
        JOptionPane.showMessageDialog(null, "Se realizÃ³ la transferencia exitosamente");
        this.cuenta.retirar(cantidad);
        this.textFieldSaldo.setText(Double.toString(this.cuenta.getSaldo()));
        this.textFieldCuentaTransferir.setText("");
        this.textFieldCantidadTransferir.setText("");
    }//GEN-LAST:event_buttonTransferirActionPerformed


